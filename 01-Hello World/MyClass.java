// A class is a must in pretty much every .java file. Declare it as public
public class MyClass
{
    /* The entry point of our program. When we run our Java project, this is
     * where BlueJ starts to execute statements within!
     */
    public static void main(String[] args)
    {
        // Print "Hello World!" to the BlueJ terminal window and end execution
        System.out.println("Hello World!");
    }
}