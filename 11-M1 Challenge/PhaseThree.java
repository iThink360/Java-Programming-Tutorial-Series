/** Phase 3: Assignment Operators */

public class PhaseThree
{
    public static void main(String[] args)
    {
        /* Pre-declaration of Variables */
        int intOne = 4;
        int intTwo = 3;
        double myDouble = 2.718;
        
        /* Mission 1 */
        intOne += 534;
        
        /* Mission 2 */
        myDouble /= 3.14;
        
        /* Mission 3 */
        intTwo *= intOne + 9;
        
        /* Mission 4 */
        System.out.println(intOne);
        System.out.println(intTwo);
        System.out.println(myDouble);
    }
}