/** Phase 2: Variables, Primitives, and Arithmetic Operators */

public class PhaseTwo
{
    public static void main(String[] args)
    {
        /* Mission 1 */
        int intOne = 4;
        int intTwo = 3;
        double myDouble = 2.718;
        
        /* Mission 2 */
        System.out.println(intOne * intTwo);
        
        /* Mission 3 */
        System.out.println(intOne + myDouble);
        
        /* Mission 4 */
        System.out.println(intTwo / myDouble);
        
        /* Mission 5 */
        System.out.println(intOne % intTwo);
        
        /* Mission 6 */
        System.out.println(4.26 + 5.3);
    }
}