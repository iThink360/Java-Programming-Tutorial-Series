public class Assignment
{
    public static void main(String[] args)
    {
        // Assignment Addition (+=)
        int myInt = 5;
        myInt += 3;
        System.out.println(myInt);
        
        // Assignment Subtraction (-=)
        myInt -= 3;
        System.out.println(myInt);
        
        // Assignment Multiplication (*=)
        myInt *= 3;
        System.out.println(myInt);
        
        // Assignment Division (/=)
        myInt /= 3;
        System.out.println(myInt);
        
        // Assignment Modulus (%=)
        myInt %= 2;
        System.out.println(myInt);
    }
}